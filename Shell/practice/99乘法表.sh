#!/bin/bash
set -eu
set -o pipefail
#--------------------------------------------
# Shell
#--------------------------------------------

for ((i = 1; i < 10; i ++)); do
	# 第一行的时候，执行打印横线
	if [[ ${i} = 1 ]]; then
		echo "------------------------------------------------"	
	fi
	
	for ((y = ${i}; y > 0; y --)); do
		echo -n "${i} x ${y} = $((i * y))	|"
	done
	echo "" # 换行
	echo "------------------------------------------------"
